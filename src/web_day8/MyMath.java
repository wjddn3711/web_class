package web_day8;

public class MyMath {
    int add(int a, int b) {
        // int result = a+b;
        // return result;
        return a + b;
    }

    int sub(int a, int b) {
        return a - b;
    }

    int times(int a, int b) {
        return a * b;
    }

    int divide(int a, int b) {
        return a / b;
    }

}
